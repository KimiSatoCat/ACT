/**
 * categorize() ユニットテスト
 *
 * カテゴリ優先度順:
 *  1. 仕事・評価
 *  2. 人間関係
 *  3. 健康・身体
 *  4. 将来への不安
 *  5. 日常の出来事
 *  6. その他（フォールバック）
 */

import { categorize } from '../categorizer';

// ─── 仕事・評価 ──────────────────────────────────────────────────────────────

describe('categorize — 仕事・評価', () => {
  test('「仕事」を含む文はカテゴリ「仕事・評価」になる', () => {
    expect(categorize('仕事でミスをしてしまった')).toBe('仕事・評価');
  });

  test('「評価」を含む文はカテゴリ「仕事・評価」になる', () => {
    expect(categorize('上司に評価されていない気がする')).toBe('仕事・評価');
  });

  test('「失敗」を含む文はカテゴリ「仕事・評価」になる', () => {
    expect(categorize('プレゼンで失敗した')).toBe('仕事・評価');
  });

  test('「ミス」を含む文はカテゴリ「仕事・評価」になる', () => {
    expect(categorize('また同じミスをした')).toBe('仕事・評価');
  });
});

// ─── 人間関係 ────────────────────────────────────────────────────────────────

describe('categorize — 人間関係', () => {
  test('「友達」を含む文はカテゴリ「人間関係」になる', () => {
    expect(categorize('友達に無視されたかもしれない')).toBe('人間関係');
  });

  test('「嫌われ」を含む文はカテゴリ「人間関係」になる', () => {
    expect(categorize('みんなに嫌われている')).toBe('人間関係');
  });

  test('「家族」を含む文はカテゴリ「人間関係」になる', () => {
    expect(categorize('家族と上手くいっていない')).toBe('人間関係');
  });
});

// ─── 健康・身体 ──────────────────────────────────────────────────────────────

describe('categorize — 健康・身体', () => {
  test('「体」を含む文はカテゴリ「健康・身体」になる', () => {
    expect(categorize('最近体の調子が悪い')).toBe('健康・身体');
  });

  test('「疲れ」を含む文はカテゴリ「健康・身体」になる', () => {
    expect(categorize('毎日疲れが取れない')).toBe('健康・身体');
  });

  test('「眠れ」を含む文はカテゴリ「健康・身体」になる', () => {
    expect(categorize('夜眠れない日が続いている')).toBe('健康・身体');
  });
});

// ─── 将来への不安 ────────────────────────────────────────────────────────────

describe('categorize — 将来への不安', () => {
  test('「将来」を含む文はカテゴリ「将来への不安」になる', () => {
    expect(categorize('将来どうなるか分からない')).toBe('将来への不安');
  });

  test('「老後」を含む文はカテゴリ「将来への不安」になる', () => {
    expect(categorize('老後のお金が心配だ')).toBe('将来への不安');
  });

  test('「不安」単体では「将来への不安」になる', () => {
    expect(categorize('なんとなく不安な気持ちがある')).toBe('将来への不安');
  });
});

// ─── 日常の出来事 ────────────────────────────────────────────────────────────

describe('categorize — 日常の出来事', () => {
  test('「買い物」を含む文はカテゴリ「日常の出来事」になる', () => {
    expect(categorize('買い物でお金を使いすぎた')).toBe('日常の出来事');
  });

  test('「忘れ」を含む文はカテゴリ「日常の出来事」になる', () => {
    expect(categorize('大事なことを忘れてしまった')).toBe('日常の出来事');
  });
});

// ─── その他（フォールバック） ────────────────────────────────────────────────

describe('categorize — その他', () => {
  test('マッチするキーワードがない文はカテゴリ「その他」になる', () => {
    expect(categorize('ふわふわした気持ちがする')).toBe('その他');
  });

  test('空文字はカテゴリ「その他」になる', () => {
    expect(categorize('')).toBe('その他');
  });

  test('スペースだけの文はカテゴリ「その他」になる', () => {
    expect(categorize('   ')).toBe('その他');
  });
});

// ─── 優先度順 ────────────────────────────────────────────────────────────────

describe('categorize — 優先度（複数キーワード一致時）', () => {
  test('「仕事」と「友達」が同時に出たとき「仕事・評価」が優先される', () => {
    // 「仕事」は優先度1, 「友達」は優先度2
    expect(categorize('仕事で友達に相談できない')).toBe('仕事・評価');
  });

  test('「友達」と「疲れ」が同時に出たとき「人間関係」が優先される', () => {
    // 「友達」は優先度2, 「疲れ」は優先度3（健康）
    expect(categorize('友達との関係が疲れる')).toBe('人間関係');
  });
});
